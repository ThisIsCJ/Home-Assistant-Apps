export const FONT_SIZES = [
  { id: 'sm', label: 'Small' },
  { id: 'md', label: 'Default' },
  { id: 'lg', label: 'Large' },
  { id: 'xl', label: 'Extra Large' },
];

export function applyAppearance({ theme = 'dark', skin = 'default', fontSize = 'md' } = {}) {
  const root = document.documentElement;
  const resolved = theme === 'system'
    ? (window.matchMedia('(prefers-color-scheme: dark)').matches ? 'dark' : 'light')
    : theme;

  const skinDef = SKINS.find(s => s.id === skin) ?? SKINS[0];
  for (const [prop, val] of Object.entries(skinDef.vars)) {
    root.style.setProperty(prop, val);
  }

  if (resolved === 'light') {
    for (const [prop, val] of Object.entries(LIGHT_VARS)) {
      root.style.setProperty(prop, val);
    }
  }

  root.setAttribute('data-size', fontSize || 'md');
}

export function saveAppearance(prefs) {
  localStorage.setItem('appearance', JSON.stringify(prefs));
}

export function loadAppearance() {
  try { return JSON.parse(localStorage.getItem('appearance') || '{}'); } catch { return {}; }
}
